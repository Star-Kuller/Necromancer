using System;
using UnityEngine;

public class SingletonObject : MonoBehaviour
{
    private static SingletonObject _instance;

    private void Awake()
    {
        if (_instance == null)
        {
            _instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else if (_instance != this)
        {
            Destroy(gameObject);
        }
    }
}
